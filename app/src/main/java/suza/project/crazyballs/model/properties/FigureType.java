package suza.project.crazyballs.model.properties;

/**
 * This enum contains various figure types.
 *
 * Created by lmark on 13/09/2017.
 */
public enum FigureType {

    /**
     * Type used far balls.
     */
    BALL,

    /**
     * Non - moving ball.
     */
    STATIC_BALL,

    /**
     * Life increase.
     */
    LIFE_BALL,

    /**
     * Bad ball.
     */
    BAD_BALL,

    /**
     * Type used for basket.
     */
    BASKET,

    /**
     * Type used for generic figures.
     */
    GENERIC,

    /**
     * Type used for star.
     */
    STAR
}
